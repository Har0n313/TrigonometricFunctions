using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Data;
using System.Drawing;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Windows.Forms;

namespace TrigonometricFunctions
{
    public partial class HelpForm : Form
    {
        private string selectedFunction;

        public HelpForm(string function)
        {
            InitializeComponent();
            selectedFunction = function;
            richTextBoxHelp.Text = GetHelpText();
        }

        private string GetHelpText()
        {
            switch (selectedFunction)
            {
                case "Sin":
                    return "Sin(x) — синус угла x.\n" +
                           "Синус угла x — это отношение противолежащего катета к гипотенузе в прямоугольном треугольнике.\n" +
                           "Формула: sin(x) = противолежащий катет / гипотенуза\n" +
                           "Синус является периодической функцией с периодом 360° (или 2π радиан).\n\n" +
                           "Основные свойства:\n" +
                           "- sin(0) = 0\n" +
                           "- sin(90°) = 1\n" +
                           "- sin(180°) = 0\n" +
                           "- sin(270°) = -1\n" +
                           "График функции синуса — это волна, колеблющаяся между -1 и 1, с нуля в 0°.\n\n" +
                           "Пример:\n" +
                           "sin(30°) = 0.5, sin(60°) ≈ 0.866\n" +
                           "sin(π/2) = 1 (если углы заданы в радианах).";

                case "Cos":
                    return "Cos(x) — косинус угла x.\n" +
                           "Косинус угла x — это отношение прилежащего катета к гипотенузе в прямоугольном треугольнике.\n" +
                           "Формула: cos(x) = прилежащий катет / гипотенуза\n" +
                           "Косинус является периодической функцией с периодом 360° (или 2π радиан).\n\n" +
                           "Основные свойства:\n" +
                           "- cos(0°) = 1\n" +
                           "- cos(90°) = 0\n" +
                           "- cos(180°) = -1\n" +
                           "- cos(270°) = 0\n" +
                           "График функции косинуса имеет фазовый сдвиг на 90° относительно синуса.\n\n" +
                           "Пример:\n" +
                           "cos(0°) = 1, cos(60°) ≈ 0.5\n" +
                           "cos(π) = -1 (если углы заданы в радианах).";

                case "Tg":
                    return "Tg(x) — тангенс угла x.\n" +
                           "Тангенс угла x — это отношение синуса угла к косинусу: tg(x) = sin(x) / cos(x).\n" +
                           "Тангенс является периодической функцией с периодом 180° (или π радиан).\n" +
                           "Тангенс имеет вертикальные асимптоты в точках, где cos(x) = 0.\n\n" +
                           "Основные свойства:\n" +
                           "- tg(0°) = 0\n" +
                           "- tg(45°) = 1\n" +
                           "- tg(90°) → ∞ (переходит в бесконечность)\n" +
                           "- tg(180°) = 0\n" +
                           "График функции тангенса имеет вертикальные асимптоты в точках 90°, 270° и так далее.\n\n" +
                           "Пример:\n" +
                           "tg(30°) ≈ 0.577, tg(60°) ≈ 1.732\n" +
                           "tg(π/4) = 1 (если углы заданы в радианах).";

                case "Ctg":
                    return "Ctg(x) — котангенс угла x.\n" +
                           "Котангенс угла x — это обратная величина тангенса: ctg(x) = 1 / tg(x).\n" +
                           "Котангенс является периодической функцией с периодом 180° (или π радиан).\n" +
                           "Котангенс имеет вертикальные асимптоты в точках, где sin(x) = 0.\n\n" +
                           "Основные свойства:\n" +
                           "- ctg(0°) → ∞ (переходит в бесконечность)\n" +
                           "- ctg(45°) = 1\n" +
                           "- ctg(90°) = 0\n" +
                           "- ctg(180°) → ∞\n" +
                           "График функции котангенса имеет вертикальные асимптоты в точках 0°, 180°, 360° и так далее.\n\n" +
                           "Пример:\n" +
                           "ctg(30°) ≈ 1.732, ctg(60°) ≈ 0.577\n" +
                           "ctg(π/4) = 1 (если углы заданы в радианах).";

                default:
                    return "Выберите функцию для получения справочной информации.";
            }
        }

        private void HelpForm_Load(object sender, EventArgs e)
        {

        }
    }
}
